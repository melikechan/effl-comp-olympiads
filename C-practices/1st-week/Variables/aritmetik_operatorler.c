#include <stdio.h>

int main(void)
{
  /*
  İşlemler:
    + -> toplama operatörü
    - -> çıkarma operatörü 
    * -> çarpma operatörü
    /  -> bölme operatörü
    % -> modül operatörü (bir sayıyla bölümünden kalanı buldurmaya yarar)
  Atama:
    = -> bir sayıyı bir değişkene atamaya yarar
    (+-/*%)= -> sağdaki değeri soldaki değer ile işleme sokar
      yani, değişkenimiz d, işlem yapılan da i olmak üzere:
      d += i -> d = d + i
      d -= i -> d = d - i
      d *= i -> d = d * i
      d /= i -> d = d / i
      d %= i -> d = d % i
  */
  return 0;
}